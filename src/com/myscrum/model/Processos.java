package com.myscrum.model;

public class Processos {
	
	static int id_processo;
	static String processo;
	static String processo_atualizar;
	
	public void setProcesso(String processo) {
		this.processo = processo;
	}
	public String getProcesso() {
		return processo;
	}
	
	public void setId_processo(int id_processo) {
		this.id_processo = id_processo;
	}
	public int getid_Processo() {
		return id_processo;
	}
	
	public void setProcesso_atualiza(String processo_atualizar) {
		this.processo_atualizar = processo_atualizar;
	}
	public String getProcesso_atualizar() {
		return processo_atualizar;
	}
	

}