package com.myscrum.model;

public class Dpto {
	
	static int id_dpto;
	static String dpto;
	static String dpto_atualizar;
	
	public void setDpto(String dpto) {
		this.dpto = dpto;
	}
	public String getDpto() {
		return dpto;
	}
	
	public void setId_dpto(int id_dpto) {
		this.id_dpto = id_dpto;
	}
	public int getId_dpto() {
		return id_dpto;
	}
	
	public void setDpto_atualiza(String dpto_atualizar) {
		this.dpto_atualizar = dpto_atualizar;
	}
	public String getDpto_atualizar() {
		return dpto_atualizar;
	}
	

}
